import numpy as np
import pandas as pd
import yfinance as yf
import tensorflow as tf
import os

from sklearn.preprocessing import StandardScaler
from sklearn.metrics import accuracy_score, precision_score, classification_report
from tensorflow.keras.models import Model
from tensorflow.keras.layers import Input, LSTM, Dense, Dropout, Attention, GlobalAveragePooling1D
from tensorflow.keras.regularizers import l2
from tensorflow.keras.callbacks import EarlyStopping, ReduceLROnPlateau

# --- MANDATE: Strict Reproducibility ---
np.random.seed(42)
tf.random.set_seed(42)

SYMBOL = 'BTC-USD'
LOOK_BACK = 30 # Reduced to 30 to capture shorter-term momentum, ignoring stale data
MODELS_DIR = 'models'
os.makedirs(MODELS_DIR, exist_ok=True)

# ==========================================
# 1. FEATURE ENGINEERING & STATIONARITY
# ==========================================
print("📥 1. Ingesting Data & Engineering Features...")
df = yf.download(SYMBOL, start='2018-01-01', end='2026-02-25', progress=False)
if isinstance(df.columns, pd.MultiIndex):
    df.columns = df.columns.get_level_values(0)

# A. Log Returns (Stationary Price)
df['Log_Ret'] = np.log(df['Close'] / df['Close'].shift(1))

# B. Volatility (Garman-Klass Proxy / ATR proxy)
df['TR'] = np.maximum(df['High'] - df['Low'],
                      np.maximum(abs(df['High'] - df['Close'].shift(1)),
                                 abs(df['Low'] - df['Close'].shift(1))))
df['ATR_14'] = df['TR'].rolling(window=14).mean()
df['Vol_20'] = df['Log_Ret'].rolling(window=20).std()

# C. Momentum (RSI)
delta = df['Close'].diff()
gain = (delta.where(delta > 0, 0)).rolling(window=14).mean()
loss = (-delta.where(delta < 0, 0)).rolling(window=14).mean()
rs = gain / loss
df['RSI_14'] = 100 - (100 / (1 + rs))

# D. Volume Flow (Stationary Volume)
df['Volume_ROC'] = df['Volume'].pct_change()

# --- MANDATE: Target Definition (Directional Alpha) ---
# 1 if tomorrow's return is positive, 0 if negative.
df['Target_Sign'] = np.where(df['Log_Ret'].shift(-1) > 0, 1, 0)

# Drop NaNs generated by shifts and rolling windows
df.dropna(inplace=True)

# WARNING CHECK: Ensure 'Close' is NOT in the feature set.
features =['Log_Ret', 'ATR_14', 'Vol_20', 'RSI_14', 'Volume_ROC']
target = 'Target_Sign'

print(f"✅ Features generated. Shape: {df.shape}")

# ==========================================
# 2. STRICT WALK-FORWARD SPLIT & SCALING
# ==========================================
# We use chronologically split data. No random train_test_split.
train_size = int(len(df) * 0.80)
train_df = df.iloc[:train_size]
test_df = df.iloc[train_size:]

# LEAKAGE PREVENTION: Scaler fits ONLY on training data.
# We use StandardScaler because Returns/RSI are normally distributed/bounded.
scaler = StandardScaler()
train_scaled = scaler.fit_transform(train_df[features])
test_scaled = scaler.transform(test_df[features])

def create_sequences(X, y, time_steps):
    Xs, ys = [],[]
    for i in range(len(X) - time_steps):
        Xs.append(X[i:(i + time_steps)])
        ys.append(y[i + time_steps])
    return np.array(Xs), np.array(ys)

y_train_arr = train_df[target].values
y_test_arr = test_df[target].values

X_train, y_train = create_sequences(train_scaled, y_train_arr, LOOK_BACK)
X_test, y_test = create_sequences(test_scaled, y_test_arr, LOOK_BACK)

# ==========================================
# 3. ARCHITECTURAL INNOVATION: LSTM + ATTENTION
# ==========================================
print("🧠 3. Compiling Attention-LSTM Architecture...")

inputs = Input(shape=(X_train.shape[1], X_train.shape[2]))

# LSTM Layer with L2 Regularization to prevent memorizing noise
x = LSTM(64, return_sequences=True, kernel_regularizer=l2(1e-4))(inputs)
x = Dropout(0.4)(x)

# Self-Attention Mechanism: Allows the model to weigh specific days
# in the 30-day window higher than others (e.g., a specific volatility spike).
attention = Attention()([x, x])

# Condense the temporal dimension
x = GlobalAveragePooling1D()(attention)

x = Dense(32, activation='relu', kernel_regularizer=l2(1e-4))(x)
x = Dropout(0.4)(x)

# Output Layer: Sigmoid for Binary Classification (Probability of UP)
outputs = Dense(1, activation='sigmoid')(x)

model = Model(inputs=inputs, outputs=outputs)

# Using Binary Crossentropy for classification
model.compile(optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
              loss='binary_crossentropy',
              metrics=['accuracy'])

early_stop = EarlyStopping(monitor='val_loss', patience=12, restore_best_weights=True)
reduce_lr = ReduceLROnPlateau(monitor='val_loss', factor=0.5, patience=5)

history = model.fit(
    X_train, y_train,
    epochs=100,
    batch_size=64, # Larger batch size for noisier data (acts as a regularizer)
    validation_split=0.1, # Uses the last 10% of TRAIN as validation
    callbacks=[early_stop, reduce_lr],
    verbose=1
)

# ==========================================
# 4. QUANTITATIVE EVALUATION (NO MSE/MAE)
# ==========================================
print("📈 4. Evaluating Alpha Generation...")

# Predict probabilities
y_pred_prob = model.predict(X_test)
# Convert probabilities to binary classes (Threshold = 0.5)
y_pred_class = (y_pred_prob > 0.5).astype(int).flatten()

# Hit Rate (Directional Accuracy)
hit_rate = accuracy_score(y_test, y_pred_class)
precision = precision_score(y_test, y_pred_class)

print("\n" + "="*50)
print(f"🎯 DIRECTIONAL PREDICTION RESULTS:")
print(f"Hit Rate (Accuracy): {hit_rate * 100:.2f}%")
print(f"Precision (When it says UP, how often is it right?): {precision * 100:.2f}%")
print("="*50)

if hit_rate > 0.60:
    print("🚨 WARNING: Accuracy > 60%. Treat with extreme skepticism. Check for look-ahead bias.")
elif hit_rate > 0.52:
    print("✅ SOLID ALPHA: A >52% hit rate in crypto with proper risk management is highly profitable.")
else:
    print("⚠️ RANDOM WALK: Model is essentially guessing (approx 50%). Needs feature refinement.")

# ==========================================
# 5. STRATEGY BACKTEST (Cumulative Returns)
# ==========================================
import matplotlib.pyplot as plt

# Align test returns with predictions
# test_df starts at train_size. Sequences eat the first LOOK_BACK days.
actual_future_returns = test_df['Log_Ret'].shift(-1).dropna().values[LOOK_BACK:]

# Truncate arrays to match length
min_len = min(len(y_pred_class), len(actual_future_returns))
y_pred_class = y_pred_class[:min_len]
actual_future_returns = actual_future_returns[:min_len]

# Strategy Logic: If prediction is 1 (UP), we hold the asset (1 * return).
# If prediction is 0 (DOWN), we stay in cash (0 * return) or short (-1 * return).
# Let's do a long-only strategy for simplicity:
strategy_returns = y_pred_class * actual_future_returns

# Calculate Cumulative Returns
cumulative_market = np.exp(np.cumsum(actual_future_returns)) - 1
cumulative_strategy = np.exp(np.cumsum(strategy_returns)) - 1

plt.figure(figsize=(14, 7))
plt.plot(cumulative_market * 100, label='Buy & Hold Market Return (%)', color='gray')
plt.plot(cumulative_strategy * 100, label='AI Strategy Return (%)', color='green', linewidth=2)
plt.title(f'Strategy Backtest (Hit Rate: {hit_rate*100:.2f}%)')
plt.ylabel('Cumulative Return (%)')
plt.xlabel('Days in Test Set')
plt.legend()
plt.grid(True, alpha=0.3)
plt.savefig(os.path.join(MODELS_DIR, 'strategy_backtest.png'))
print("✅ Strategy backtest chart saved.")